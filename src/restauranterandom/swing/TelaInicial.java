    public void populaListaComObjetos() {
        PopulaRestaurante popular = new PopulaRestaurante();
        List<Restaurante> listaRestaurante = new ArrayList<>();
        listaRestaurante = popular.popularRestaurantes();

        for (int i = 0; i < listaRestaurante.size(); i++) {
            lista.addElement(listaRestaurante.get(i));
        }

        jListaRestaurante.setModel(lista);
    }

    public void gerarResultadoRandom() {
        double random = Math.random() * lista.getSize();
        int valor = (int) random;
        jLabelResultadoGerado.setText(lista.get(valor).toString());
    }

