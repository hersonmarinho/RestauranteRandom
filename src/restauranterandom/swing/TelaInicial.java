    private void jButtonLimparListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimparListaActionPerformed
        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não há restaurantes na lista!");
        } else {
            lista.clear();
            JOptionPane.showMessageDialog(rootPane, "A lista de restaurantes foi limpa!!");
            jLabelResultadoGerado.setText(new String());
        }
    }//GEN-LAST:event_jButtonLimparListaActionPerformed

